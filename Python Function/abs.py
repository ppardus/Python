# Python'da abs() fonksiyonu, bir sayının mutlak değerini döndürür. Yani, sayının pozitif değerini verir. Eğer sayı negatifse, mutlak değerini alarak pozitif hale getirir.

# Örneğin:

num = -10
print(abs(num))

# Yukarıdaki örnekte, num değişkenine atanan değer -10 olmasına rağmen, abs() fonksiyonu sayesinde 10 çıktısı elde edilir.