# bool() fonksiyonu, bir ifadeyi veya nesneyi True veya False olarak değerlendirir. 
# Eğer ifade veya nesne boş, sıfır, None veya False ise False olarak değerlendirilir, 
# aksi halde True olarak değerlendirilir. 

# Örneğin:

print(bool(0))      # False
print(bool(10))     # True
print(bool(""))     # False
print(bool("hello")) # True
print(bool([]))     # False
print(bool([1, 2]))  # True
