# Python'da chr() fonksiyonu, ASCII değerini alır ve o değerin karşılık geldiği karakteri döndürür.
# Bu fonksiyon, bir karakterin ASCII değerini bulmak ya da bir ASCII değerine sahip olan karakteri 
# bulmak için kullanılabilir.

# Örneğin, ASCII değeri 97 olan karakter "a" dır. Aşağıdaki örnek, chr() fonksiyonunu kullanarak 
# ASCII değeri 97 olan karakteri bulur ve ekrana yazdırır:

# print(chr(97))

# Bu kod, "a" karakterini ekrana yazdıracaktır.

# Benzer şekilde, aşağıdaki örnek, "A" karakterinin ASCII değerini bulur ve ekrana yazdırır:

# print(ord("A"))

# Bu kod, "A" karakterinin ASCII değerini olan 65'i ekrana yazdıracaktır.
